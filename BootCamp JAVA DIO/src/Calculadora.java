
public class Calculadora {

	public static void soma(double numero1, double numero2) {
		double resultado = numero1 + numero2;

		System.out.println("A soma do numero: " + numero1 + " mais " + numero2 + " eh " + resultado);
	}

	public static void subtração(double numero1, double numero2) {
		double resultado = numero1 - numero2;

		System.out.println("A subtração do numero: " + numero1 + " menos " + numero2 + " eh " + resultado);

	}

	public static void multiplicação(double numero1, double numero2) {
		double resultado = numero1 * numero2;

		System.out.println("A multiplicação do numero: " + numero1 + " vezes " + numero2 + " eh " + resultado);

	}

	public static void divisao(double numero1, double numero2) {
		double resultado = numero1 / numero2;

		System.out.println("A divisão do numero: " + numero1 + " dividido " + numero2 + " eh " + resultado);

	}
	
	
	
}
